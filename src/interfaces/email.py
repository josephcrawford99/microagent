import email
import email.mime.text
import email.utils
import imaplib
import logging
import os
import smtplib
from dataclasses import dataclass
from typing import ClassVar, Optional

from lib.interface import Interface, Message, Trigger

log = logging.getLogger("microagent.email")


@dataclass
class EmailMessage(Message):
    """Email payload — adds `subject` to the base body/to/sender fields."""

    subject: str = ""

    SCHEMA: ClassVar[dict] = {"to": str, "subject": str, "body": str}


@dataclass
class EmailTrigger(Trigger):
    unread: int


class Email(Interface):
    """IMAP/SMTP email — direct, no filesystem inbox.

    The agent sees `email_send(to, subject, body)` and `email_receive()` —
    auto-generated by the base Interface from EmailMessage.SCHEMA.
    """

    name = "email"
    message_class = EmailMessage

    def __init__(self, config):
        super().__init__(config)
        self.imap_host = config["imap_host"]
        self.imap_port = config.get("imap_port", 993)
        self.smtp_host = config["smtp_host"]
        self.smtp_port = config.get("smtp_port", 587)
        self.username = config["username"]
        self.password = os.environ.get(config.get("password_env", "EMAIL_PASSWORD"), "")
        self.allowed_senders = [s.lower() for s in config.get("allowed_senders", [])]

    # --- lifecycle ---

    def trigger_wake(self) -> Optional[EmailTrigger]:
        try:
            imap = self._imap()
            try:
                status, data = imap.search(None, "UNSEEN")
                if status != "OK":
                    return None
                count = len(data[0].split())
            finally:
                imap.close()
                imap.logout()
        except Exception:
            log.exception("trigger_wake: imap search failed")
            return None
        if count == 0:
            return None
        return EmailTrigger(interface=self, unread=count)

    # --- I/O ---

    async def receive(self) -> list[EmailMessage]:
        out: list[EmailMessage] = []
        imap = self._imap()
        try:
            status, data = imap.search(None, "UNSEEN")
            if status != "OK":
                raise RuntimeError(f"IMAP SEARCH failed: {status}")
            for msg_id in data[0].split():
                status, msg_data = imap.fetch(msg_id, "(RFC822)")
                if status != "OK":
                    continue
                parsed = email.message_from_bytes(msg_data[0][1])
                sender = email.utils.parseaddr(parsed["From"])[1].lower()
                if self.allowed_senders and sender not in self.allowed_senders:
                    log.info("ignoring email from non-allowed sender: %s", sender)
                    continue
                out.append(
                    EmailMessage(
                        body=self._extract_body(parsed),
                        sender=sender,
                        to=self.username,
                        subject=parsed.get("Subject", "") or "",
                    )
                )
        finally:
            imap.close()
            imap.logout()
        return out

    async def send(self, message: Message) -> str:
        if not message.to:
            raise RuntimeError("email send requires `to` to be set on the message")

        subject = getattr(message, "subject", "") or "(no subject)"

        mime = email.mime.text.MIMEText(message.body)
        mime["Subject"] = subject
        mime["From"] = self.username
        mime["To"] = message.to
        with smtplib.SMTP(self.smtp_host, self.smtp_port) as server:
            server.starttls()
            server.login(self.username, self.password)
            server.send_message(mime)
        log.info("sent email to %s: %s", message.to, subject)
        return f"sent to {message.to}"

    # --- helpers ---

    def _imap(self) -> imaplib.IMAP4_SSL:
        imap = imaplib.IMAP4_SSL(self.imap_host, self.imap_port)
        imap.login(self.username, self.password)
        imap.select("INBOX")
        return imap

    def _extract_body(self, parsed) -> str:
        if parsed.is_multipart():
            for part in parsed.walk():
                if part.get_content_type() == "text/plain":
                    payload = part.get_payload(decode=True)
                    if payload:
                        return payload.decode("utf-8", errors="replace")
            return ""
        payload = parsed.get_payload(decode=True)
        if payload:
            return payload.decode("utf-8", errors="replace")
        return ""
